package main.entity.Enum;

/**
 * Enumeration representing the types of flats available in the BTO system.
 * Each type defines the size and configuration of the housing unit.
 */
public enum FlatType {
    
    /** A two-room flat, typically smaller and suitable for singles or couples. */
    TWO_ROOM,

    /** A three-room flat, larger than two-room, suitable for small families. */
    THREE_ROOM;
}
